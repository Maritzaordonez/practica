package edu.tectii.restaurantes2;


class ElementoRestaurante {
    int imagenRestaunte;
    String nombreRestaurante, descripcionRestaurante, direccionRestaurante;
    public ElementoRestaurante (int imageRest, String nombreRest, String descripcionRest, String direccionRest) {
        this.imagenRestaunte = imageRest;
        this.nombreRestaurante = nombreRest;
        this.descripcionRestaurante = descripcionRest;
        this.direccionRestaurante = direccionRest;
    }
}
